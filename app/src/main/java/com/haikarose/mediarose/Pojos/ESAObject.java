public class ESAObject {

    private Long id;
    private String brand_name;
    private String promo_image;
    private String promo_message;
    private int day_start;
    private int day_end;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getBrand_name() {
        return brand_name;
    }

    public void setBrand_name(String brand_name) {
        this.brand_name = brand_name;
    }

    public String getPromo_image() {
        return promo_image;
    }

    public void setPromo_image(String promo_image) {
        this.promo_image = promo_image;
    }

    public String getPromo_message() {
        return promo_message;
    }

    public void setPromo_message(String promo_message) {
        this.promo_message = promo_message;
    }

    public int getDay_start() {
        return day_start;
    }

    public void setDay_start(int day_start) {
        this.day_start = day_start;
    }

    public int getDay_end() {
        return day_end;
    }

    public void setDay_end(int day_end) {
        this.day_end = day_end;
    }



}
